            <div>
                <strong><h4 id="Mariposa">Conejo Mariposa</h4></strong>
                <p>Dentro de las distintas razas de conejo, esta también es conocida como English Spot. Debe su peculiar nombre a la mancha de color de la nariz que recuerda la silueta de una mariposa con las alas desplegadas. También tiene una línea o raya en otro color que se prolonga desde la nuca a la cola.</p>
                <ul class="container">
                    <li><strong>De dónde procede: </strong>el papillón francés, este “pequeño” gigante, es originario de Alsacia, 
                    región ubicada en Francia.</li>
                    <li><strong>Origen de la raza: </strong>Conocido también como conejo mariposa, su nombre indica claramente 
                    sus orígenes: se trata de una mezcla de papillon, por el tipo de piel y el color, y conejo gigante, en cuanto 
                    al peso. En Alemania se le conoce desde hace más de un siglo. Sus antepasados serían papillones franceses y 
                    conejos japoneses. Se llama así a causa de la mancha de su nariz que, vista de frente, hace pensar en una 
                    mariposa (papillon en francés) con las alas desplegadas.</li>
                    <li><strong>Dónde vive: </strong>el hábitat de esta especie de conejo se encuentra en Alsacia y en Alemania, 
                    donde muchos criadores no han dejado de mejorar sus cualidades para los concursos de belleza.</li>
                    <li><strong>Cría: </strong>es precoz, prolífica y de carne delicada, tales son las ventajas de esta raza. 
                    Los jóvenes crecen muy deprisa (entre 40 g y 45 g por día) y adquieren rápidamente un lomo grueso y muslos 
                    carnosos. Es útil también para los cruces. En este caso, hay que evitar cruzar hembras durante su primer 
                    acoplamiento.</li>
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Flandes">Conejo gigante de flandes</h4></strong>
                <p>Los ejemplares más grandes de conejo gigante de Flandes (que pesan de 6 kg a 8 kg) se quedan cortos ante otros 
                individuos que pueden alcanzar los 15 kg. Este coloso de cuerpo largo, poderosa musculatura y fuerte osamenta, 
                solo necesita alrededor de seis meses para alcanzar su peso ideal.</p>
                <ul class="container">
                    <li><strong>De dónde procede: </strong>este tipo de conejo es originario de la región de Gante en Bélgica.</li>
                    <li><strong>Origen de la raza: </strong>este impresionante conejo sería el resultado de haber sobrealimentado a la raza silvestre y realizado cruces basados en una fuerte consanguinidad. El estándar de la raza fue reconocido en Francia en 1919.</li>
                    <li><strong>Dónde vive: </strong>su hábitat se encuentra en numerosas conejeras de corral donde sirve sobre todo como raza mejoradora.</li>
                    <li><strong>Cría: </strong>suele ser un animal que se cría bastante debido la buena calidad de su carne y su función como macho mejorador. Por curiosidad y por la belleza de su piel, podemos criar uno o dos ejemplares. Sí lo que buscamos es la producción de carne para el consumo familiar, bastará con cruzarlos con conejos de tamaño mediano, que le aportarán cierta precocidad.</li>

                </ul>
            </div>
            <br><hr><br>
            <div>
                    <strong><h4 id="Hotot">Conejo blanco de hotot</h4></strong>
                    <p>El conejo blanco de hotot tiene su origen en Francia. Fue creado en el año 1902 por el criador Eugenie 
                    Bernhard en la región de Hotot-en-Auge después de varios cruces. Desde esa fecha no ha parado de crecer 
                    su popularidad debido a su dulce aspecto y enternecedora apariencia. A los más pequeños de la casa les encanta.</p>
                    <p>Al ser de muy pequeño tamaño, lo hace ideal para tenerlos en un hogar de reducidas dimensiones. 
                    Del mismo modo, requiere a diario un rato para corretear en libertad para hacer ejercicio.</p>
                    <p>Tiene un carácter algo miedoso, pero con el paso del tiempo se acostumbran a la presencia humana. 
                    Es entonces cuando disfrutarás de una mascota dócil y muy tranquila. Suele estar muy demandado pese a que 
                    no existen demasiados criadores. Esto hace que pueda resultar algo caro a la hora de comprar.</p>
                    <p>Sin duda alguna, es una de las más bonitas razas de conejo. La característica física más destacable 
                    es que cuenta con unos grandes ojos castaños, rodeados por un círculo negro que resalta sobre su pelo 
                    blanco. Se debe controlar su alimentación, ya que es bastante glotón y puede tener tendencia a la obesidad.</p>
                </div>
                <br><hr><br>
            <div>
                <strong><h4 id="Ruso">Conejo ruso</h4></strong>
                <p>Todo el mundo conoce a esta raza con el iris pigmentado de un color rosa rojizo. El primer Estándar de esta 
                raza ligera data de 1910. El conejo ruso está entre las razas más apreciadas por los criadores debido a su peso, no muy 
                voluminoso aunque regular, que se adapta muy bien a las exigencias del mercado.</p>
                <ul class="container">
                        <li><strong>De dónde procede: </strong>contrariamente a lo que su nombre podría hacer creer, proviene de Inglaterra.</li>
                        <li><strong>Origen de la raza: </strong>a este respecto existen varias hipótesis. La más admitida habitualmente achaca su origen a una mutación de conejos de tipo plateado. Otras lo consideran fruto de cruces entre conejos plateados negros y grises, o con aportación de sangre de chinchilla. Al conejo ruso se le conoce con otros nombres que dan fe de la confusión respecto a sus orígenes, desde conejo del Himalaya en los países anglosajones, a conejo chino, egipcio e incluso africano.</li>
                        <li><strong>Dónde vive: </strong>es una de las razas más presentes en todos los países como mascota.</li>
                        <li><strong>Cría: </strong>estos conejos tienen un buen rendimiento en carne y muy poca grasa. Para un criador aficionado, es preferible optar por razas más pesadas y mantener ésta como animal de compañía o mascota, lo que hará las delicias de los niños del entorno.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Angora">Conejo de angora</h4></strong>
                <p>El Angora Rabbit-book atribuye puntos a cada conejo en función de la producción de pelo. El peso depende de 
                la longitud de las fibras (9 cm a 11 cm), diferente para cada individuo. La hembra, cuyo pelo es de mejor 
                calidad, debe producir entre 210 y 280 g. La materia obtenida a partir de esos pelos sirve para confeccionar ropa.</p>
                <ul class="container">
                        <li><strong>De dónde procede: </strong>el conejo de angora francés nació en la región de Aix-les- Bains.</li>
                        <li><strong>Origen de la raza: </strong>Diderot y D’Alembert citaban a este conejo en su Enciclopedia. No es, por tanto, una raza nueva. Ha sido objeto de mucha atención y ha evolucionado mucho, con el objetivo de producir un conejo con más pelo. De 250 g anuales en cuatro recogidas de pelo, ha pasado a 250 g por recogida. El libro genealógico fue creado en 1956.</li>
                        <li><strong>Dónde vive: </strong>los criadores, que cuentan con unas 500 jaulas cada uno, están diseminados prácticamente por la mitad de los departamentos franceses.</li>
                        <li><strong>Cría: </strong>La producción francesa no garantiza la cobertura de la demanda, por lo que las importaciones, esencialmente de China y Argentina, representan más de tres veces la producción francesa.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Californiano">Conejo californiano</h4></strong>
                <p>Los conejos californianos cuentan con una extraordinaria capa de pelo. Es una mascota muy recomendable y apreciada. Esto hace que se pueda encontrar en millones de hogares de todo el mundo. Apareció oficialmente en 1928 en una exposición y se reconoció oficialmente en 1939 por la ARBA.</p>
                <ul class="container">
                        <li><strong>De dónde procede: </strong> esta raza es el resultado del cruce entre conejos chinchilla e himalaya.</li>
                        <li><strong>Origen de la raza: </strong>como su propio nombre indica, se originaron en California (EE.UU.).</li>
                        <li><strong>Dónde vive: </strong> prácticamente en su totalidad se dedican a su uso como mascota.</li>
                        <li><strong>Cría: </strong> no requiere atenciones especiales diferentes a la de otros conejos. Deben disponer de una jaula con el suficiente espacio, juguetes para que puedan entretenerse y un clima ni demasiado caluroso ni muy frío.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Rex">Conejo rex</h4></strong>
                <p>Una de las características de la raza de conejo Rex es que no tiene bigotes. Se han clasificado 15 variedades 
                diferentes de este animal según el color de su piel. Aunque éste varía, la estructura de la piel sigue siendo 
                la misma.</p>
                <ul class="container">
                        <li><strong>De dónde procede: </strong>el origen geográfico de este tipo de conejo se encuentra en la región francesa de Sharte.</li>
                        <li><strong>Origen de la raza: </strong>la forma conocida como “rex castor” fue seleccionada por el abad Gillet, que vivió en Coulans-sur-Gée a principios del siglo XX. No hace falta decir que debe el nombre al color de su piel, que se parece a la del castor (marrón intenso con la base de los pelos azulada). A imitación de André Wiltzer que obtuvo el rex rojizo, otros criadores se dedicaron a desarrollar diferentes colores de pelaje.</li>
                        <li><strong>Dónde vive: </strong>esta raza está muy extendida y se encuentra en toda Francia y otros países de Europa.</li>
                        <li><strong>Cría: </strong>no es un animal que requiera excesivos cuidados ni atenciones para su cría. Suele ser precoz y se permiten cruces entre todas sus variedades existentes. La piel, sobre todo la del dorso en donde los pelos están especialmente desarrollados, se emplea en peletería.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Cabeza_Leon">Conejo cabeza de leon</h4></strong>
                <p>El conejo cabeza de león, también conocido como lionhead, es una de las razas de conejos más famosas en todo 
                el mundo. Y esto es debido a su peculiar y largo pelaje que se asimila a la melena de un león.</p>
                <ul class="container">
                        <li><strong>Comportamiento: </strong>es una muy buena mascota para aquellas personas que les encanta tener durante horas a un conejo entre los brazos. Le encanta los mimos y caricias. Además, es muy calmado y simpático.</li>
                        <li><strong>Origen de la raza: </strong>el origen del conejo cabeza de león lo encontramos en Bélgica. Muchos criadores de conejos lo utilizan como raza “mejoradora” para otras especies. En concreto se cruza mucho con el conejo belier.</li>
                        <li><strong>Cría: </strong>  las hembras suelen tener entre 4 y 9 crías por parto. La gestación dura unos 28 días. Durante este tiempo es aconsejable alimentar a la coneja con una dieta rica en proteínas.</li>
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Tan">Conejo tan</h4></strong>
                <p>El conejo tan recuerda muchísimo por el color de su pelo a un perro Dóberman o a un Rottweiler. 
                Originarios de Inglaterra, se ha mejorado su carácter mediante cruces para lograr una mascota dulce y muy 
                inteligente</p>
                <ul class="container">
                        <li><strong>Comportamiento: </strong>goza de un carácter dulce y simpático. Muestra una actitud alerta y destaca su gran curiosidad e inteligencia. Es un conejo que necesita altas dosis de ejercicio y espacio para poder moverse, saltar y corretear a diario.</li>
                        <li><strong>Origen de la raza:</strong>la raza de conejo Tan surgió en la localidad de Derbyshire (Inglaterra) en el año 1887. Fruto del cruce de conejos holandeses y de conejos silvestres.</li>
                        <li><strong>Color: </strong>predomina el color negro con zonas anaranjadas o color fuego. Existen también las variedades lila, chocolate y azul.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Bouscat">Bouscat blanco gigante</h4></strong>
                <p>Este conejo tiene sus poderosos cuartos traseros particularmente desarrollados. Posee un precioso pelo, 
                largo y de un blanco escarchado, al que la falta total de pigmentación confiere una cierta majestad y elegancia.</p>
                <p>Es uno de los conejos gigantes más interesantes, fácil de cruzar con los neozelandeses y rusos</p>
                <ul class="container">
                        <li><strong>De dónde procede: </strong>este conejo procede de la región francesa de Gironde, concretamente de Bouscat.</li>
                        <li><strong>Origen de la raza: </strong>El matrimonio Dulon, criadores de la región de Bouscat, tuvieron la idea de cruzar conejos plateados de Champagne con gigantes de Flandes y angoras. El resultado fue el conejo blanco de Bouscat gigante, originalmente conocido como conejo armiñado. Fue presentado oficialmente en un concurso en París en 1910.</li>
                        <li><strong>Dónde vive: </strong>el conejo blanco gigante de Bouscat es muy popular entre los criadores que mantienen la raza pura o cruzada.</li>
                        <li><strong>Cría: </strong>es un animal vigoroso que puede ser utilizado en cruces destinados a mejorar el modelo de ciertas razas de tamaño mediano, como el neozelandés o el ruso. Su precocidad y la finura de su carne hacen de este conejo de cuerpo alargado y robusto, con musculatura poderosa y equilibrada, un productor de carne muy recomendable, Es una de las razas gigantes con mejor relación hueso-carne.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Belier">Belier francés</h4></strong>
                <p>Este conejo grande es reconocible de inmediato por sus orejas, con pelo y consistentes, que pueden medir 
                entre 38 cm y 45 cm. Aunque debe gran parte de su popularidad a sus orejas colgantes y muy largas, esta raza 
                resulta especialmente interesante por el tamaño de sus lomos y sus gruesos muslos.</p>
                <ul class="container">
                        <li><strong>De dónde procede:</strong>este conejo es producto de los cruces entre ejemplares ingleses y franceses. Un enorme “peluche” de encantador aspecto.</li>
                        <li><strong>Origen de la raza</strong> el estándar de la raza de esta clase de conejo fue reconocido en 1922, gracias al esfuerzo de un encuadernador llamado Cordonnier. Cruzó beliers ingleses, que estaban siendo importados desde 1853 (y rebautizados beliers franceses), con conejos gigantes de Flandes y conejos normandos. Esta raza debe su nombre a su gruesa frente, que recuerda a la de un cordero.</li>
                        <li><strong>Dónde vive: </strong>los aficionados al belier francés son especialmente numerosos en Alemania, en el este de Francia y en provincias limítrofes.</li>
                        <li><strong>Cría: </strong>tiene un crecimiento más lento que el de las razas de tamaño mediano y una fertilidad a veces menor. El tamaño y el peso del belier impiden su cría sobre rejillas metálicas, ya que sus patas no lo soportarían Para que los jóvenes crezcan más deprisa es preferible suministrarles una alimentación más rica y abundante, pero siempre equilibrada.</li>
    
                </ul>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Neozelandes">Conejo neozelandes</h4></strong>
                <p>Su pelaje albino hace reparar de inmediato en este conejo robusto, prototipo del animal de carne. 
                Esta raza data de la década de 1950, lo que no le ha impedido convertirse en una de las más interesantes, 
                tanto para la cría intensiva como para los aficionados.</p>
                <p>Algunos aprecian su capacidad para ser criado sobre suelos de enrejado, mientras que otros valoran su 
                aspecto de mejorador de otras razas. Aunque de raza americana, está muy extendida actualmente en todos 
                los criaderos.</p>
                <p>Resulta muy ventajoso su cruce con cualquier otra raza criada para carne, a la que transmite su carácter 
                precoz, su lomo bien lleno y su musculatura compacta y regular. Pesa alrededor de 4,5 kg o 5 kg. 
                Ostenta el récord de reproducción con cinco o seis camadas anuales que producen de tres a siete gazapos cada una.</p>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Enano">Conejo enano</h4></strong>
                <p>El conejo enano es como su nombre indica… ¡superpequeño! El peso de los conejos enanos adultos puede variar 
                según la raza. Puede oscilar entre los 800g-1kg de un ejemplar de Netherland dwarf hasta los 2,380kg de un dwarf 
                lop. Su aspecto es realmente adorable gracias a sus orejas cortas y redondeadas, su nariz chata y su cuerpo 
                reducido. Además, tiene un pelo muy, muy suave que invita continuamente a las caricias. Importante: cuando 
                queramos cogerle para acariciarle o sacarle de la jaula, tendremos que hacerlo con mucho cuidado ya que sus 
                huesos pueden fracturarse fácilmente.</p>
                <p>El conejo enano de color se caracteriza por ser una mascota juguetona que desde la década de 1960 ha 
                conquistado el corazón de los niños. Creado a partir del polaco, mediante el cruce con conejos de color, 
                puede ser de todos los tonos conocidos.</p>
                <p>Según los criadores de conejos enanos, los más atractivos son el negro, el azul, el plateado y el siamés. 
                Solo presenta un pequeño inconveniente para los que quieran dedicarse a su cría: no tiene, o prácticamente nunca
                , más de dos o tres crías por camada.</p>
            </div>
            <br><hr><br>
            <div>
                <strong><h4 id="Mini_Lop">Conejo mini lop</h4></strong>
                <p>Los Mini Lop son una pequeña raza de conejos cuyo peso ideal oscila entre 1,5 y 1,6 kilogramos 
                (3,3 y 3.52 libras). En el estándar de la raza, el cuerpo de un ejemplar debe ser corto, ancho y 
                bien musculazo, y su coloración puede variar en diferentes colores.</p>
                <p>La mayoría de los conejos pueden llevarse bien con otros animales como conejillos de indias, 
                si estos son introducidos en su ambiente adecuadamente. Los Mini lops no son la excepción a esto. 
                De hecho, fueron desarrollados con la intención de crear un conejo fácil de manejar y adaptar, y lo 
                suficientemente resistente para soportar abrazos de los niños.</p>
                    
