                <strong><h4 id="Introduccion">Introducción</h4></strong>
                <p>Conocer las razas de conejos y sus características es una premisa básica si nuestra intención es adoptar a 
                un conejo. Al igual que sucede con perros y gatos estas adorables mascotas tendrán personalidad propia así como 
                un comportamiento o un aspecto físico particular.</p>
